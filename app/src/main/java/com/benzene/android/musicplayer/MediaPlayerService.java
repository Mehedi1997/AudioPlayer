    private enum FadeState {
        FADE_IN,
        FADE_OUT
    }

    private void fade(MediaPlayer player, FadeState fadeState) {
        final int FADE_DURATION = 1200;
        final int FADE_INTERVAL = 100;
        int numberOfSteps = FADE_DURATION / FADE_INTERVAL;
        final float deltaVolume;
        if (fadeState.equals(FadeState.FADE_OUT)) {
            deltaVolume = -1f / numberOfSteps;
        } else {
            deltaVolume = 1f / numberOfSteps;
        }
        final Timer timer = new Timer(true);
        TimerTask timerTask = new TimerTask() {
            float volume = fadeState == FadeState.FADE_IN ? 0.0f : 1.0f;

            @Override
            public void run() {
                volume += deltaVolume;
                player.setVolume(volume, volume);
                switch (fadeState) {
                    case FADE_IN:
                        if (volume >= 1.0f) {
                            timer.cancel();
                            timer.purge();
                        }
                        break;
                    case FADE_OUT:
                        if (volume <= 0.0f) {
                            player.pause();
                            timer.cancel();
                            timer.purge();
                        }
                        break;
                }
            }
        };
        timer.schedule(timerTask, FADE_INTERVAL, FADE_INTERVAL);
    }

    @Override
    public void onAudioFocusChange(int focusState) {
        //Invoked when the audio focus of the system is updated.
        switch (focusState) {
            case AudioManager.AUDIOFOCUS_GAIN:
                // resume playback
                if (mediaPlayer == null) initMediaPlayer();
                else if (!mediaPlayer.isPlaying()) mediaPlayer.start();
                //mediaPlayer.setVolume(1.0f, 1.0f);
                fade(mediaPlayer, FadeState.FADE_IN);
                break;
            case AudioManager.AUDIOFOCUS_LOSS:
                // Lost focus for an unbounded amount of time: stop playback and release media player
                if (mediaPlayer.isPlaying()) mediaPlayer.stop();
                mediaPlayer.release();
                mediaPlayer = null;
                break;
            case AudioManager.AUDIOFOCUS_LOSS_TRANSIENT:
            case AudioManager.AUDIOFOCUS_LOSS_TRANSIENT_CAN_DUCK:
                // Lost focus for a short time, but it's ok to keep playing
                // at an attenuated level
                // Lost focus for a short time, but we have to stop
                // playback. We don't release the media player because playback
                // is likely to resume
                if (mediaPlayer.isPlaying()) {
                    fade(mediaPlayer, FadeState.FADE_OUT);
                    //mediaPlayer.pause();
                }
                break;
            //mediaPlayer.setVolume(0.1f, 0.1f);
        }
    }

